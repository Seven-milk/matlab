function [ y,x ] = mt_menu( y0,x0 )
%[ y,x ] = mt_menu( y0,x0 )
%   Add a menu for mutation test.
%   
%   AUTHOR sfhstcn2
%   CONTACT sfh_st_cn2@163.com

if nargin<2
    x0 = [];
end
if nargin<1
    y0 = [];
end
y = y0;
x = x0;

hm = uimenu('label','Mutation Test','callback',['detl = whos;' ... 
    'for i = 1:length(detl);H1{i} = detl(i).name;end;' ... 
    'if isempty(find(strcmp(H1,''IH'')));IH=[];end;' ... 
    'if isempty(find(strcmp(H1,''p'')));p=[];end;' ... 
    'if isempty(find(strcmp(H1,''L0'')));L0=[];end;' ... 
    'if isempty(find(strcmp(H1,''scale'')));scale=[];end;clear detl H1 i']);
hci = uimenu(hm,'label','Information');
hcic1 = uimenu(hci,'label','Moving t-test','callback','help Movet');
hcic2 = uimenu(hci,'label','Cramer Method','callback','help Cramert');
hcic3 = uimenu(hci,'label','Yamamoto Method','callback','help Yamamoto');
hcic4 = uimenu(hci,'label','Mann Kendall','callback','help MannKendall');
hcic5 = uimenu(hci,'label','Pettitt Method','callback','help Pettitt');
hcic6 = uimenu(hci,'label','Le Page Method','callback','help LePage');
hcic7 = uimenu(hci,'label','BGSA','callback','help BGSA');
hcic8 = uimenu(hci,'label','Wavelet Method','callback','help wvlt_trans');
hc1 = uimenu(hm,'label','Moving t-test','callback', ... 
    ['op=inputdlg({''IH'',''p''},''Parameters'',1,{num2str(IH),num2str(p)});'...
    'if ~isempty(op);IH=str2num(op{1});p=str2num(op{2});clf;[ t,tp ] = Movet( y,x,IH,p );mt_menu;tool_isf_menu;clear op;end']);
hc2 = uimenu(hm,'label','Cramer Method','callback', ... 
    ['op=inputdlg({''n1'',''p''},''Parameters'',1,{num2str(IH),num2str(p)});'...
    'if ~isempty(op);IH=str2num(op{1});p=str2num(op{2});clf;[ t,tp ] = Cramert( y,x,IH,p );mt_menu;tool_isf_menu;clear op;end']);
hc3 = uimenu(hm,'label','Yamamoto Method','callback', ... 
    ['op=inputdlg({''IH'',''p''},''Parameters'',1,{num2str(IH),num2str(p)});'...
    'if ~isempty(op);IH=str2num(op{1});p=str2num(op{2});clf;[ Rsn,Rp ] = Yamamoto( y,x,IH,p );mt_menu;tool_isf_menu;clear op;end']);
hc4 = uimenu(hm,'label','Mann Kendall','callback', ... 
    ['op=inputdlg({''p''},''Parameters'',1,{num2str(p)});'...
    'if ~isempty(op);p=str2num(op{1});clf;[ UF,UB ] = MannKendall( y,x,p );mt_menu;tool_isf_menu;clear op;end']);
hc5 = uimenu(hm,'label','Pettitt','callback', ... 
    'clf;[ t0,P ] = Pettitt( y,x );mt_menu;tool_isf_menu;clear op;');
hc6 = uimenu(hm,'label','Le Page Method','callback', ... 
    ['op=inputdlg({''IH'',''p''},''Parameters'',1,{num2str(IH),num2str(p)});'...
    'if ~isempty(op);IH=str2num(op{1});p=str2num(op{2});clf;[ WA,chip ] = LePage( y,x,IH,p );mt_menu;tool_isf_menu;clear op;end']);
hc7 = uimenu(hm,'label','BGSA','callback', ... 
    ['op=inputdlg({''p'',''L0''},''Parameters'',1,{num2str(p),num2str(L0)});'...
    'if ~isempty(op);p=str2num(op{1});L0=str2num(op{2});clf;[ FLAGS,my ] = BGSA( y,x,p,L0 );mt_menu;tool_isf_menu;clear op;end']);
hc8 = uimenu(hm,'label','Wavelet Method','callback', ... 
    ['op=inputdlg({''scale'',''p''},''Parameters'',1,{num2str(scale),num2str(p)});'...
    'if ~isempty(op);scale=str2num(op{1});p=str2num(op{2});clf;[ wvcoef ] = wvlt_trans( y,x,scale,p );mt_menu;tool_isf_menu;clear op;end']);

end

